import {GameScene} from "../Basic/GameScene";
import {Enemy} from "../Enemy";
import {setScene} from "../../Engine/Engine";

export const startRoom: () => any = function () {
    const map = [
        '#######################################################',
        '#                                                    ##',
        '#                                                     #',
        '# o o **                                              #',
        '#  o                                                  #',
        '#                                                     >',
        '# #########     #######################################',
        '# #########     #######################################',
        '# #########            ################################',
        '# ##########   ####### ################################',
        '# ##########                     ######################',
        '# ################ ############# ######################',
        '# ################ ############# ######################',
        '# ################ ############  ######################',
        '# ################ ###########  #######################',
        '# ################ ##########  ########################',
        '# ################ #########  #########################',
        ': :::#############           ##########################',
        ':   :##################################################',
        '::::::::::::::::::::::::::::::::::::::::::::::::::::::::',
    ];
    const decoder = {
        ' ': 'empty',
        '#': 'block',
        '*': 'coinbox',
        'o': 'coin',
        '>': 'empty',
        ':': "deepGround",
    };
    const scene: GameScene = new GameScene();
    scene.initializeMap(map, decoder);
    scene.initializePlayer(3, 5);
    scene.add(new Enemy(10, 5, 0.1, 0));
    scene.add(new Enemy(12, 5, 0.1, 0));
    setScene(scene);
}

